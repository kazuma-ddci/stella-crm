import { prisma } from "@/lib/prisma";
import Link from "next/link";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { TransactionsTable } from "./transactions-table";
import type { TransactionListItem } from "./actions";

export default async function TransactionsPage() {
  const [records, counterparties] = await Promise.all([
    prisma.transaction.findMany({
      where: { deletedAt: null },
      include: {
        counterparty: true,
        expenseCategory: true,
        costCenter: true,
        allocationTemplate: true,
        creator: true,
      },
      orderBy: [{ periodFrom: "desc" }, { id: "desc" }],
    }),
    prisma.counterparty.findMany({
      where: { deletedAt: null, isActive: true },
      orderBy: { id: "asc" },
    }),
  ]);

  const formatDate = (date: Date | null): string | null => {
    if (!date) return null;
    return date.toISOString().split("T")[0];
  };

  const data: TransactionListItem[] = records.map((r) => ({
    id: r.id,
    type: r.type,
    counterpartyId: r.counterpartyId,
    counterpartyName: r.counterparty.name,
    expenseCategoryId: r.expenseCategoryId,
    expenseCategoryName: r.expenseCategory.name,
    costCenterName: r.costCenter?.name ?? null,
    allocationTemplateName: r.allocationTemplate?.name ?? null,
    amount: r.amount,
    taxAmount: r.taxAmount,
    taxRate: r.taxRate,
    taxType: r.taxType,
    periodFrom: r.periodFrom.toISOString().split("T")[0],
    periodTo: r.periodTo.toISOString().split("T")[0],
    paymentDueDate: formatDate(r.paymentDueDate),
    scheduledPaymentDate: formatDate(r.scheduledPaymentDate),
    status: r.status,
    note: r.note,
    isAutoGenerated: r.isAutoGenerated,
    sourceType: r.sourceType,
    invoiceGroupId: r.invoiceGroupId,
    paymentGroupId: r.paymentGroupId,
    contractId: r.contractId,
    createdByName: r.creator.name,
    createdAt: r.createdAt.toISOString().split("T")[0],
    updatedAt: r.updatedAt.toISOString().split("T")[0],
  }));

  const counterpartyOptions = counterparties.map((c) => ({
    value: String(c.id),
    label: c.name,
  }));

  // サマリー計算
  const calcTaxIncluded = (r: { amount: number; taxAmount: number; taxType: string }) =>
    r.taxType === "tax_excluded" ? r.amount + r.taxAmount : r.amount;
  const totalRevenue = records
    .filter((r) => r.type === "revenue")
    .reduce((sum, r) => sum + calcTaxIncluded(r), 0);
  const totalExpense = records
    .filter((r) => r.type === "expense")
    .reduce((sum, r) => sum + calcTaxIncluded(r), 0);
  const unconfirmedCount = records.filter((r) => r.status === "unconfirmed").length;
  const returnedCount = records.filter((r) => r.status === "returned" || r.status === "resubmitted").length;

  return (
    <div className="space-y-6">
      <div className="flex flex-col gap-3 sm:flex-row sm:items-center sm:justify-between">
        <div>
          <h1 className="text-2xl font-bold">取引管理</h1>
          <p className="text-sm text-muted-foreground">
            取引化した候補の詳細入力・修正・確定・経理引渡を行います。
          </p>
        </div>
        <div className="flex flex-wrap gap-2">
          <Button asChild variant="outline" size="sm">
            <Link href="/stp/finance/generate">取引候補を確認</Link>
          </Button>
          <Button asChild variant="outline" size="sm">
            <Link href="/stp/finance/counterparties">取引先管理</Link>
          </Button>
          <Button asChild size="sm">
            <Link href="/stp/finance/transactions/new">手動で取引追加</Link>
          </Button>
        </div>
      </div>

      <div className={`grid grid-cols-1 gap-4 ${returnedCount > 0 ? "sm:grid-cols-4" : "sm:grid-cols-3"}`}>
        <Card>
          <CardContent className="pt-6">
            <div className="text-sm text-muted-foreground">売上合計（税込）</div>
            <div className="text-2xl font-bold text-emerald-600">
              ¥{totalRevenue.toLocaleString()}
            </div>
          </CardContent>
        </Card>
        <Card>
          <CardContent className="pt-6">
            <div className="text-sm text-muted-foreground">経費合計（税込）</div>
            <div className="text-2xl font-bold text-rose-600">
              ¥{totalExpense.toLocaleString()}
            </div>
          </CardContent>
        </Card>
        <Card>
          <CardContent className="pt-6">
            <div className="text-sm text-muted-foreground">未確定</div>
            <div className="text-2xl font-bold text-orange-600">
              {unconfirmedCount}件
            </div>
          </CardContent>
        </Card>
        {returnedCount > 0 && (
          <Card>
            <CardContent className="pt-6">
              <div className="text-sm text-muted-foreground">差し戻し</div>
              <div className="text-2xl font-bold text-red-600">
                {returnedCount}件
              </div>
            </CardContent>
          </Card>
        )}
      </div>

      <Card>
        <CardHeader>
          <CardTitle>取引一覧</CardTitle>
        </CardHeader>
        <CardContent>
          <TransactionsTable
            data={data}
            counterpartyOptions={counterpartyOptions}
          />
        </CardContent>
      </Card>
    </div>
  );
}
